#include "3-calc.h"
/**
 * op_add - sums its parameters
 * @a: param 1
 * @b: param2
 *
 * Return: value of added integers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts its parameters
 * @a: param 1
 * @b: param2
 *
 * Return: value of subtracted integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies its parameters
 * @a: param 1
 * @b: param2
 *
 * Return: value of multiplied integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides its parameters
 * @a: param 1
 * @b: param2
 *
 * Return: division value
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns remainder of division of two integers
 * @a: param 1
 * @b: param2
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
